function updateActiveLanguageButton(language) {
    const ruButton = document.getElementById("ruButton");
    const kzButton = document.getElementById("kzButton");

    if (!ruButton || !kzButton) {
        return;
    }

    ruButton.classList.toggle(
        "active",
        language === "ru"
    );

    kzButton.classList.toggle(
        "active",
        language === "kz"
    );
}


function getInitialLanguage() {
    const savedLanguage =
        localStorage.getItem("orleuLanguage");

    if (
        savedLanguage === "ru" ||
        savedLanguage === "kz"
    ) {
        return savedLanguage;
    }

    const browserLanguage =
        navigator.language.toLowerCase();

    if (
        browserLanguage.startsWith("kk") ||
        browserLanguage.startsWith("kz")
    ) {
        return "kz";
    }

    return "ru";
}


document.addEventListener(
    "DOMContentLoaded",
    function () {
        setLanguage(getInitialLanguage());
    }
);
